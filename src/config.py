"""K230 runtime configuration.

APIs: no hardware API calls; values configure Sensor, Display, cv_lite, and UART.
Hardware: Yahboom K230 12Pin module.
Runtime: CanMV K230 Yahboom v1.8.0 MicroPython.
"""

# -----------------------------------------------------------------------------
# 摄像头配置
# -----------------------------------------------------------------------------
# cv_lite.rgb888_find_circles() 要求输入 RGB888，因此 main.py 会把 Sensor
# 输出格式设置成 Sensor.RGB888。640x480 适合第一阶段：画面足够清楚，内存和
# 运算量也比 1080P 小得多。
CAMERA_WIDTH = 640
CAMERA_HEIGHT = 480
CAMERA_HMIRROR = False
CAMERA_VFLIP = False
# CanMV 当前 API 使用 auto_exposure() 控制自动曝光。
# 不配置“自动增益”：官方当前接口只有部分传感器支持的 again() 手动模拟增益。
CAMERA_AUTO_EXPOSURE = True
CAMERA_WARMUP_MS = 1000

# -----------------------------------------------------------------------------
# LCD 配置
# -----------------------------------------------------------------------------
# Yahboom 12Pin 模块常见屏幕为 ST7701 800x480。若电脑 B 上板时屏幕不亮，
# 先对照该模块自带“摄像头显示”例程确认 DISPLAY_TYPE，而不是随意猜测 API。
DISPLAY_TYPE = "ST7701"
DISPLAY_WIDTH = 800
DISPLAY_HEIGHT = 480
DISPLAY_TO_IDE = True

# -----------------------------------------------------------------------------
# 黄色轨道检测配置（本阶段真正使用）
# -----------------------------------------------------------------------------
# cv_lite 的 RGB 阈值顺序：
# [R最小, R最大, G最小, G最大, B最小, B最大]
#
# 下列数值是根据项目照片给出的“起始阈值”，不是标定结果：
# - 黄色通常 R、G 较高；
# - B 通道通常较低；
# - 现场光照、曝光和白平衡变化后必须重新调节。
TRACK_RGB_THRESHOLD = [130, 255, 90, 255, 0, 150]

# cv_lite 在内部按真实连通区域面积过滤；640x480 下先从 1200 像素起步。
# 如果轨道完全检测不到可逐步减小；如果出现小黄物体干扰则增大。
TRACK_MIN_AREA = 1200

# 第一次上板先使用官方 Blob 示例常见值 1，减少参数差异。
# 若后续出现很多离散噪点，可再尝试改成 3 并重新记录 FPS/轮廓变化。
TRACK_KERNEL_SIZE = 1

# ROI 格式：(x, y, width, height)。第一遍测试使用全画面，保证先看到结果。
# 相机固定后可缩小，例如 (20, 100, 600, 280)，排除桌面和人体背景。
# 当前 cv_lite Blob API 不接收 ROI，本项目在检测结果阶段做 ROI 筛选。
TRACK_ROI = (0, 0, CAMERA_WIDTH, CAMERA_HEIGHT)

# 黄色轨道应当明显细长。Blob 外接框长宽比小于该值时判为干扰物。
TRACK_MIN_BBOX_ASPECT_RATIO = 2.5

# 控制台每隔多少帧输出一次，避免 print 严重拖慢帧率。
CONSOLE_INTERVAL_FRAMES = 10

DEBUG_IMAGE_ENABLED = True
# 本阶段专用文件名，避免与后续钢球 Debug 图片混淆。
DEBUG_IMAGE_PATH = "/sdcard/ball_debug.jpg"
DEBUG_SAVE_INTERVAL_MS = 5000
GC_INTERVAL_FRAMES = 30

# -----------------------------------------------------------------------------
# UART：K230向STM32单向发送真实视觉测量
# -----------------------------------------------------------------------------
UART_ENABLED = True
UART_ID = 1
UART_BAUDRATE = 115200
UART_TX_PIN = 9
UART_RX_PIN = 10

# -----------------------------------------------------------------------------
# 机械模型参数预留（当前设备程序不使用）
# -----------------------------------------------------------------------------
# 这些数值只记录当前零位约定、人工安全范围和一级近似模型的粗测初值。
# 它们尚未通过正反向标定，不得直接作为闭环控制、限位保护或精确补偿依据。
SERVO_ZERO_DEG = 0.0
RAIL_ZERO_OFFSET_DEG = 0.0
SERVO_LIMIT_DEG = 90.0
MODEL_R_SERVO_CM = 1.5
MODEL_L_DRIVE_CM = 24.8

# -----------------------------------------------------------------------------
# 钢球静态检测配置（当前阶段使用）
# -----------------------------------------------------------------------------
# 当前钢球在深色Blob阈值下会与背景/阴影形成大连通区域，因此正式链路
# 使用已在Yahboom v1.8.0实机验证的霍夫圆检测。
# 当前现场已把左边界扩展到x=10。检测ROI可以比软件安全区更宽：
# 这样球虽然已经越过安全线，K230仍可报告“看得见但不安全”。
BALL_ROI = (10, 205, 620, 90)
BALL_CIRCLE_DP = 1
BALL_CIRCLE_MIN_DIST = 30
BALL_CIRCLE_PARAM1 = 80
BALL_CIRCLE_PARAM2 = 20
BALL_CIRCLE_MIN_RADIUS = 8
BALL_CIRCLE_MAX_RADIUS = 35

# -----------------------------------------------------------------------------
# 钢球连续跟踪参数
# -----------------------------------------------------------------------------
# 首次找到多个圆且没有上一帧位置参考时，优先选择半径最接近17 px的圆。
# 17来自当前相机距离下多次静态实测的典型钢球半径，不是物理半径。
BALL_EXPECTED_RADIUS = 17

# 已经跟踪到钢球后，只接受距离上一帧不超过80 px的候选圆。
# 该限制用于排除突然跳到远处反光点；若高速滚动时连续丢球，可适当增大。
BALL_TRACK_MAX_JUMP_PX = 80

# 相邻有效帧的半径变化不能超过8 px，用于排除突然变大的反光圆。
# 日志中的正常半径主要为13～20 px，因此8 px保留了较宽余量。
BALL_TRACK_MAX_RADIUS_CHANGE = 8

# 连续3帧没有合格候选后，忘记旧位置并允许在全ROI重新捕获钢球。
# 在约20 FPS下对应约0.15秒；在此期间每个失败帧仍立即输出ball_valid=0。
BALL_TRACK_LOST_RESET_FRAMES = 3

# 一阶指数滤波系数：0.75表示新测量占75%，历史占25%。
# 数值越大响应越快但越抖；当前优先保证滚动跟踪的实时性。
BALL_FILTER_ALPHA = 0.75

# 2026-07-21三点实测：轨道物理中心在x=360～362，暂取361作为控制零点。
# 画面右侧对应固定端，因此error_px = TARGET_X - ball_x：
#   error_px < 0 固定端；error_px = 0 中心；error_px > 0 舵机端。
BALL_TARGET_X = 361

# x约24和625时钢球已贴近画面边缘。闭环前先把60～598定义为软件安全区，
# 避免把仍能偶尔检出的半截钢球误当作正常可控状态。
BALL_SAFE_LEFT_X = 60
BALL_SAFE_RIGHT_X = 598
